package BasicsInJava;

public class StringConcatenationInJava2 {
	
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		double c = 10.44;
		double d = 20.56;
		char e = 'A';
		String s1 = "Itachi";
		String s2 = "Uchiha";
		
		//"+' is concatenation operator
		System.out.println(a+b);
		System.out.println(c+d);
		System.out.println(s1+s2);
			
	}

}
