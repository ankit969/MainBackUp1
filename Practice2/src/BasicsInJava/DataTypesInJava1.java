package BasicsInJava;

public class DataTypesInJava1 {
	
	public static void main(String[] args) {
		
		//Int DataType
		int a = 10;
		int b = 20;
		int c = a+b;
		System.out.println("Integer DataType: " +c);
		
		//Double DataType:
		double d1 = 10.11;
		double d2 = 20.22;
		double d3 = d1+d2;
		System.out.println("Double DataType: " +d3);
		
		//char DataType:
		char c1 = 'M';
		char c2 = '$';
		System.out.println("Char DataType: " +c2);
		
		//Boolean Datatype:
		boolean b1 = true;
		boolean b2 = false;
		System.out.println("Boolean DataType: " +b2);
		
		//String class:
		String s1 = "Hello";
		String s2 = "World";
		System.out.println("String class: " +s2);
	}

}
