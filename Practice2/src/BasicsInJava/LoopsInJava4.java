package BasicsInJava;

public class LoopsInJava4 {
	
	public static void main(String[] args) {
		
		//while loop:
		int i = 1;
		while(i<=5) {
			System.out.println(i);
			i++;
			
		//for loop:
		//print 1 to 10
		for(int j=0; j<=10;j++) {
			System.out.println(j);
		}
		//print 10 to 1:
		for(int k=10; k>1; k--) {
			System.out.println(k);
		}
		}
	}

}
