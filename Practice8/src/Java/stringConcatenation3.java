package Java;

public class stringConcatenation3 {
	
	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		double d1 = 30.55;
		double d2 = 19.45;
		String s1 = "Hello";
		String s2 = "World";
		
		System.out.println(d1+d2);
		System.out.println(x+y);
		System.out.println(x+y+s1);
		System.out.println(s1+x+y);
	}

}
