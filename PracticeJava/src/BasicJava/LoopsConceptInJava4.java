package BasicJava;

public class LoopsConceptInJava4 {
	
	public static void main(String[] args) {
		
	
	//while loop:
	int i = 0;
	while( i<=5) {
		System.out.println(i);
		i = i+1;
	}
	//for loop:
	
	//print 1 to 10
	for(int a=0; a<=10; a++) {
		System.out.println(a);
	}
	//print 10 to 1
	for(int j=10; j>=1; j--) {
		System.out.println(j);
		
	}
	
	}
	

}
