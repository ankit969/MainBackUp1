package BasicJava;

public class DataTypesInJava1 {
	
	public static void main(String[] args) {
		
		//int DataType
		int x = 10;
		int y = 20;
		int z = x+y;
		System.out.println(z);
		
		//double DataType
		double d1 = 10.00;
		double d2 = 20.44;
		double d3 = d1+d2;
		System.out.println(d3);
		
		//char DataType
		char c1 = 'A';
		char c2 = '$';
		System.out.println(c1);
		
		//boolean DataType
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);
		
		//String Class
		String s1 = "Hello";
		String s2 = "1000";
		System.out.println(s1);
		System.out.println(s2);
	}

}
